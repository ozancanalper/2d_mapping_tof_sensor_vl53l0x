% --- Executes on button press in Start.
function Start_Callback(hObject, eventdata, handles)
% hObject    handle to Start (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
comport = serial('COM3','Baudrate',115200); % create comport 
fopen(comport); % This command was used to interact with the defined comport.
axes(handles.axes1); %Prepares the drawing screen in the GUI
x=1; % to enter an infinite loop
while(x)
    A=fscanf(comport,'%d %d'); % data is read as angle and distance.
    polarplot (A(1)*pi/180, A(2),'.'); %
    drawnow
    title('Mapping around the Robot (mm)');
    thetalim([0 180]); % For determining the range of polarplot 
    grid on; 
    hold on
end
fclose(comport);
delete(comport);

